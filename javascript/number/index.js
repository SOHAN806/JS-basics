// the number is a built-in object that represents numeric values

// Number.MAX_SAFE_INTEGER: method represents the maximum safe integer in JavaScript
const c = Number.MAX_SAFE_INTEGER;
console.log(c);

// Number.MIN_SAFE_INTEGER: method represents the minimum safe integer in JavaScript
const d = Number.MIN_SAFE_INTEGER;
console.log(d);

// Number.NaN: method is short for "Not-a-Number
const g = Number.NaN;
console.log(g);

// Number.parseFloat(): method parses a value as a string and returns the first number
const h = Number.parseFloat("10.33");
console.log(h);

// Number.parseInt(): method parses a value as a string and returns the first integer
const i = Number.parseInt("10");
console.log(i);

// toFixed(): method converts a number to a string
const num = 5.56789;
let n = num.toFixed();
console.log(n);
